//判断一个整数是否是回文数。回文数是指正序（从左向右）和倒序（从右向左）读都是一样的整数。
//
//示例 1:
//
//输入 : 121
//输出 : true
//	 示例 2 :
//
// 输入 : -121
//  输出 : false
//   解释 : 从左向右读, 为 - 121 。 从右向左读, 为 121 - 。因此它不是一个回文数。
//		示例 3 :
//
//	输入 : 10
//	 输出 : false
//	  解释 : 从右向左读, 为 01 。因此它不是一个回文数。
#define _CRT_SECURE_NO_WARNINGS 
#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
bool isPalindrome(int x) {
	int a = 0;
	if (x<0||(x != 0 && x % 10 == 0))
	{
		return false;
	}
	while (x>a)
	{
		a = a * 10 + x % 10;
		x /= 10;
	}
	return (x == a || (x == a / 10));


}
int main()
{
	int x = 0;
	scanf("%d", &x);
	bool a = isPalindrome(x);
	printf("%d ", a);
	system("pause");
	return 0;
}


